void solve()
{
    ll n;
    cin>>n;
    string s; 
    cin>>s;
    vector<int>id ;
    for(ll i=0;i<n;i++)    
    {
        if(s[i]=='2')
            id.push_back(i);
    }
    int k = id.size();
    if(k==1||k==2)
    {
        cout<<"NO"<<endl;
        return;
    }
    vector<string>t(n,string(n,'='));
    for( ll i =0 ;i<n;i++) t[i][i]='X';
    for(ll i=0;i<k;i++)
    {
        int x = id[i];
        int y = id[(i+1)%k];
        t[x][y] = '+';
        t[y][x] = '-';
    }
    cout<<"YES"<<endl;
    for(int i =0;i<n;i++)
    {
        cout<<t[i]<<endl;
    }
}

